package Java_B1;

public class DeclareAndUseVariables {
    public static void main(String[] args) {
        int i = 10; // khai báo biến i là kiểu int có giá trị là 10
        float f = 20.5f; //khai báo biến f là kiểu float có giá trị 20.5
        double d = 20.5; //khai báo biến d là kiểu double có giá trị 20.5
        boolean b = true; //khai báo biến b là kiểu boolean có giá trị true
        char c = 'a'; //khai báo biến c là kiểu char có giá trị là ký tự a
        String s = "Hà Nội"; //khai báo biến s có kiểu là String có giá trị là chuỗi Hà nội
        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
    }
}
